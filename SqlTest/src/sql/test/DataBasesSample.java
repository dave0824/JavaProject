package sql.test;

import java.sql.Connection;
import java.sql.DriverManager;
import java.sql.ResultSet;
import java.sql.SQLException;
import java.sql.Statement;

/**
 * 这是一个连接数据库的一般流程
 * @author Superhero
 * @version 2018年12月22日上午11:08:54
 */
public class DataBasesSample {
 
    public static void main(String[] args) {
        //声明Connection对象
        Connection con;
        //驱动程序名
        String driver = "com.mysql.jdbc.Driver";
        //URL指向要访问的数据库名mydata        String url = "jdbc:mysql://localhost:3306/sqltestdb";
        //MySQL配置时的用户名         String user = "root";
         //MySQL配置时的密码
         String password = "763081703";
         String url="jdbc:mysql://localhost:3306/mydatabase?useUnicode=true&characterEncoding=UTF-8&useSSL=false";
         //另一种形式，localhost等价于127.0.0.1
         //String url = "jdbc:mysql://127.0.0.1:3306/mydatabase?useUnicode=true&characterEncoding=UTF-8&useSSL=false";
         //遍历查询结果集
        try {
            //加载驱动程序
            Class.forName(driver);
             //1.getConnection()方法，连接MySQL数据库！！
            con = DriverManager.getConnection(url,"root",password);
             if(!con.isClosed())
                 System.out.println("Succeeded connecting to the Database!");
             //2.创建statement类对象，用来执行SQL语句！！
             Statement statement = con.createStatement();
             //要执行的SQL语句
            String sql = "select * from Student";
           //3.ResultSet类，用来存放获取的结果集！！
            ResultSet rs = statement.executeQuery(sql);
            System.out.println("-----------------");
             System.out.println("执行结果如下所示:");  
             System.out.println("-----------------");  
             System.out.println("id" + "\t" + "姓名"+ "\t" + "姓别"+ "\t" + "生日"+ "\t\t" + "系别");  
             System.out.println("-----------------");  
              
             String name = null;
             int id ;
             int sex;
             String brith = null;
             String dept = null;
            while(rs.next()){
                 //获取stuid这列数据
                id = rs.getInt("Sid");
                //获取stuname这列数据
                name = rs.getString("Sname");
                
                sex = rs.getInt("Ssex");
                
                brith = rs.getString("Sbrith");
                
                dept = rs.getString("Sdept");
                //输出结果
            System.out.println(id + "\t" + name + "\t" + sex + "\t" + brith + "\t" + dept);
             }
             rs.close();
             con.close();
         } catch(ClassNotFoundException e) {   
            //数据库驱动类异常处理
            System.out.println("Sorry,can`t find the Driver!");   
            e.printStackTrace();   
            } catch(SQLException e) {
            //数据库连接失败异常处理
             e.printStackTrace();  
            }catch (Exception e) {
            // TODO: handle exception
             e.printStackTrace();
         }finally{
             System.out.println("数据库数据成功获取！！");
         }
   }
 }